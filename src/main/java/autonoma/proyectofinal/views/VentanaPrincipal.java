    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String nombre_paciente = txtNombre.getText();
        String apellido_paciente = txtApellido.getText();
        String edad_texto = txtEdad.getText();

        if (nombre_paciente.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor ingrese el nombre del paciente");
            return;
        }

        if (apellido_paciente.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor ingrese el apellido del paciente");
            return;
        }

        if (edad_texto.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor ingrese la edad del paciente");
            return;
        }

        int edad_paciente;
        try {
            edad_paciente = Integer.parseInt(edad_texto);

            if (edad_paciente <= 0) {
                JOptionPane.showMessageDialog(this, "La edad debe ser mayor a 0");
                return;
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "La edad debe ser un número válido");
            return;
        }

        Paciente paciente = new Paciente();
        try {
            paciente.insertarPacienteBD(nombre_paciente, apellido_paciente, edad_paciente);

            VentanaSintomas ventananasintomas = new VentanaSintomas(this, true);
            ventananasintomas.setLocationRelativeTo(null);
            this.dispose();
            ventananasintomas.setVisible(true);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error al guardar en la base de datos");
            Logger.getLogger(VentanaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1MouseClicked


